`GRP1_CD`    VARCHAR(20)  NULL     COMMENT '그룹코드1', -- GRP1_CD > B11
`GRP2_CD`    VARCHAR(20)  NULL     COMMENT '그룹코드2', -- GRP2_CD > B12
