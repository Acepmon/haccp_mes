`ITEM_NM`    VARCHAR(100) NULL     COMMENT '자재이름', -- ITEM_NM
`SPEC`       VARCHAR(100) NULL     COMMENT '규격', -- SPEC
`UNIT`       VARCHAR(20)  NULL     COMMENT '단위', -- UNIT
`QTY1`       DECIMAL(10)  NULL     COMMENT '당수량', -- QTY1
`QTY2`       DECIMAL(10)  NULL     COMMENT '봉수량', -- QTY2
`CONN_NO`    DECIMAL(10)  NULL     COMMENT '대표품목', -- CONN_NO
`CONN_QTY`   DECIMAL(10)  NULL     COMMENT '연결품목', -- CONN_QTY
