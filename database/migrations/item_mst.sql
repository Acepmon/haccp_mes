`PROCESS_CD` VARCHAR(20)  NULL     COMMENT '공정코드', -- PROCESS_CD > B14
`UNIT1_NM`   VARCHAR(30)  NULL     COMMENT '대단위명', -- UNIT1_NM
`UNIT1_QTY`  DECIMAL(10)  NULL     COMMENT '대단위수', -- UNIT1_QTY
